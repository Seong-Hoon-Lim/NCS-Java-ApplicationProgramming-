package com.kudangtang.jv200.mod004;

public class IncrementOperator {
	public static void main(String[] args) {
		int num = 3;
		int result = num++;
		System.out.println(num);
		System.out.println(result);
		
		num = 3;
		result = ++num;
		System.out.println(num);
		System.out.println(result);
	}
}
